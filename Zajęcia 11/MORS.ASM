	MOV DPTR, #NAPIS
	MOV A, R3
	MOVC A,@A+DPTR
	JZ STOP	
	PUSH ACC
	LCALL WRITE_DATA
	POP ACC
	LCALL MORSE
	INC R3
	SJMP LOOP

STOP:
	SJMP	STOP
MORSE:

	SUBB A, #'A' ;ODLEG£OŒÆ W ALFABECIE
	RL A
	MOV DPTR, #DATA_M
	INC A
	PUSH ACC
	MOVC A, @A+DPTR
	MOV R1, A
	POP ACC
	DEC A
	MOVC A, @A+DPTR
	MOV R2, A
LOOP1:
	MOV A, R2
	RLC A
	MOV R2, A
	CLR P1.7
	JC L11
	MOV A, #2
	SJMP L12
L11:
	MOV A, #6
L12:
	LCALL DELAY_100MS
	SETB P1.7
	MOV A, #4
	LCALL DELAY_100MS
	DJNZ R1, LOOP1
	MOV A, #10
	LCALL DELAY_100MS
	RET
