
	LJMP	START
	ORG	100H
START:
	LCALL LCD_CLR
	
LOOP:	LCALL WAIT_KEY
	ADD A, #'0'
	LCALL WR_DATA
	
	SJMP LOOP

WR_DATA:
	MOV R0, #LCDWD
	MOV R1, #LCDRC
	PUSH ACC

L11:
	MOVX A, @R1
	JB ACC.7, L11

	CJNE A, #10H, L12
	MOV A, #40H
	SJMP L14
	
L12:

	CJNE A, #50, L13
	MOV A, #00H

L14:
	SETB ACC.7
	MOV R0, #LCDWC
	MOVX @R0, A
	
L13:

	MOV R0, #LCDWC

	POP ACC
	MOVX @R0, A
	RET
