
	LJMP	START
	ORG	100H
START:
	MOV A, #10000000B
	MOV R0, #CS55D
	MOVX @R0, A
	MOV A, #11111111B
	MOV R0, #CS55A
	MOVX @R0, A
	LCALL LCD_CLR
	

LOOP:
	MOV R1, #8	

	
LOOP2:	
	MOV DPTR, #SYGNALIZACJA
	MOV A, R1
	DEC A
	MOVC A, @A+DPTR
	CPL A
	MOV R0, #CS55B
	MOVX @R0, A
	LCALL WRITE_HEX

	MOV DPTR, #CZAS
	MOV A, R1
	DEC A
	MOVC A, @A+DPTR
	LCALL DELAY_100MS
	
	DJNZ R1, LOOP2
	
	

	SJMP	LOOP

