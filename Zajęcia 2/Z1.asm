	LJMP START

	ORG 100H

START:	

	LCALL LCD_CLR

LC1:

	LCALL WAIT_KEY

	CJNE A, #10, L1

L1:	
	JNC LC1

	MOV R1, A

	LCALL WRITE_HEX


LC2:

	LCALL WAIT_KEY

	CJNE A, #10, L2

L2:	

	JNC LC2

	MOV R2, A

	LCALL WRITE_HEX

	MOV A, R1

	SWAP A

	ORL A, R2

	MOV 100, A

	LCALL WRITE_HEX

	MOV 101, #0

	MOV R0, #100
	
	LCALL BCD_HEX

	MOV A, @R0

	LCALL WRITE_HEX

STOP:

	SJMP STOP