	LJMP START

	ORG 100H

START:	

	LCALL LCD_CLR

	LCALL WAIT_KEY	

	MOV R1, A

	LCALL WRITE_HEX

	MOV A, #'+'

	LCALL WRITE_DATA

	LCALL WAIT_KEY

	MOV R2, A

	LCALL WRITE_HEX

	MOV A, #'='

	LCALL WRITE_DATA

	MOV A, R1

	ADD A, R2

	MOV R3, A

	LCALL WRITE_HEX

STOP:

	SJMP STOP