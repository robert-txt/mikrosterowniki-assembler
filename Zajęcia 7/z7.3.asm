	LJMP	START
	ORG	100H

START:
	LCALL LCD_CLR
	
LOOP:
	
	MOV R3, #6
	MOV R1, #CSDB
	MOV A, #00000001B
	MOVX @R1, A

	MOV R0, #CSDS
	MOV A, #00000001B
L1:
	SETB P1.6
	MOVX @R0, A

	CLR P1.6
	
	RL A
	
	PUSH ACC
	MOV A, #100
	LCALL DELAY_MS
	POP ACC

	DJNZ R3, L1
	SJMP LOOP


	
	
STOP:
	SJMP	STOP
