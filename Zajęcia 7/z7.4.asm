	LJMP	START
	ORG	100H

START:
	LCALL LCD_CLR
	MOV R4, #6
LOOP:
	
	MOV R3, #6
	MOV R1, #CSDB
	
L3:	MOV A, R4
	MOV DPTR, #DIG
	MOVC A, @A+DPTR
	MOVX @R1, A
	

L2:	MOV R0, #CSDS
	MOV A, #00000001B
L1:
	SETB P1.6
	MOVX @R0, A

	CLR P1.6
	
	RL A
	
	PUSH ACC
	MOV A, #100
	LCALL DELAY_MS
	POP ACC

	DJNZ R3, L1
	DJNZ R4, L3
	SJMP LOOP
	


	
	
