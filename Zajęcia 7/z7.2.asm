	LJMP	START
	ORG	100H

START:
	LCALL LCD_CLR

	LCALL WAIT_KEY
	
	MOV R1, #CSDB
	MOV DPTR, #DIG
	MOVC A, @A+DPTR
	MOVX @R1, A

	MOV R0, #CSDS
	MOV A, #00000001B
	MOVX @R0, A


	CLR P1.6
	
STOP:
	SJMP	STOP

DIG:
	DB 00111111B; KOD 0
	DB 00000110B; KOD 1
	DB 01011011B; 2
	DB 01001111B; 3
	DB 01100110B; 4
	DB 01101101B; 5
	DB 01111101B; 6
	DB 00000111B; 7
	DB 01111111B; 8
	DB 01101111B; 9

